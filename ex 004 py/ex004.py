algo = input('digite algo : ')
print(f'o tipo primitivo eh {type(algo)}')
print(f'soh tem espacos? {algo.isspace()}')
print(f'eh um numero? {algo.isnumeric()}')
print(f'eh alfabetico? {algo.isalpha()}')
print(f'eh alfanumerico? {algo.isalnum()}')
print(f'estah em maiusculas? {algo.isupper()}')
print(f'estah em minusculas? {algo.islower()}')
print(f'estah capitalizada? {algo.istitle()}')




